#!/bin/bash
# Reliquary — prep-worker bootstrap (no wallet, no bittensor, no submit).
#
# Sets up the dependencies needed to run scripts/prep_prompt_outcomes.py
# on a sibling GPU box. The prep worker pre-populates the shared Supabase
# prompt_outcomes table with (prompt_idx, checkpoint_hash, k, σ, status)
# rows so the live miner skips known-bad prompts on launch.
#
# Stack: torch 2.11.0+cu130, transformers 5.8 with FA2 (kernels-community
# fallback), supabase-py for persistence. Generation goes through HF —
# the SAME kernel path the miner and validator use — so σ values match
# bit-for-bit and pregen rollouts pass GRAIL / reward-distribution checks.
# (Earlier vLLM-based prep produced 0% GRAIL-pass; reverted to HF.)
#
# Usage:
#   # Edit SUPABASE_*, HF_TOKEN, VALIDATOR_URL below, then:
#   bash /root/reliquary/setup_prep.sh
#
#   source scripts/prep.env
#   bash scripts/launch_prep.sh                       # one worker per GPU
#
# To stop:
#   pkill -f prep_prompt_outcomes

set -euo pipefail

# ─────────────────────────────────────────────────────────────────────
# Operator config — EDIT these before running on the prep box.
# ─────────────────────────────────────────────────────────────────────

HF_TOKEN="${HF_TOKEN:-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}"

# Validator endpoint — the prep script polls /state to learn the
# currently-published checkpoint (so prep σ values match the miner's).
VALIDATOR_URL="${VALIDATOR_URL:-http://86.38.238.30:8080}"

# Shared Supabase project. Must be the SAME project the miner is using
# — that's the whole point: prep box and miner box write/read the same
# prompt_outcomes table. KEY must be a service_role JWT.
SUPABASE_URL="${SUPABASE_URL:-https://fwyglzgpodpflkdoibdh.supabase.co}"
SUPABASE_KEY="${SUPABASE_KEY:-eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6ImZ3eWdsemdwb2RwZmxrZG9pYmRoIiwicm9sZSI6InNlcnZpY2Vfcm9sZSIsImlhdCI6MTc3OTcyNTM1NywiZXhwIjoyMDk1MzAxMzU3fQ.LsqslSQ1ruA7vuef4MUKMOhrun64eQR7aymaQMi3XGg}"

INSTALL_DIR="${INSTALL_DIR:-/root/reliquary}"
VENV_DIR="${VENV_DIR:-/root/.venv}"

# ─────────────────────────────────────────────────────────────────────
# Pre-flight
# ─────────────────────────────────────────────────────────────────────

if [ ! -d "$INSTALL_DIR" ]; then
  echo "ERROR: $INSTALL_DIR missing. Clone the repo first:"
  echo "  git clone <repo-url> $INSTALL_DIR"
  exit 1
fi

if ! command -v nvidia-smi >/dev/null 2>&1; then
  echo "ERROR: nvidia-smi missing. The prep worker needs a CUDA-capable GPU."
  exit 1
fi

echo "[prep-setup] GPU detected:"
nvidia-smi --query-gpu=name,memory.total --format=csv,noheader | head -2 | sed 's/^/  /'

if ! command -v uv >/dev/null 2>&1; then
  curl -LsSf https://astral.sh/uv/install.sh | sh
  export PATH="$HOME/.local/bin:$PATH"
fi

if [ ! -d "$VENV_DIR" ]; then
  uv venv "$VENV_DIR" --python 3.12
fi

echo "[prep-setup] installing reliquary editable"
VIRTUAL_ENV="$VENV_DIR" uv pip install -e "$INSTALL_DIR"

echo "[prep-setup] pinning torch==2.11.0+cu130"
VIRTUAL_ENV="$VENV_DIR" uv pip install \
  'torch==2.11.0' \
  --index-url https://download.pytorch.org/whl/cu130 \
  --extra-index-url https://pypi.org/simple

echo "[prep-setup] pinning transformers==5.8.0 + kernels + supabase"
VIRTUAL_ENV="$VENV_DIR" uv pip install 'transformers==5.8.0' 'kernels' 'supabase'

echo "[prep-setup] verifying stack"
"$VENV_DIR/bin/python" - <<'PY'
import torch, transformers, supabase, sys
print(f"python: {sys.version.split()[0]}")
print(f"torch:  {torch.__version__}  cuda={torch.version.cuda}")
print(f"transformers: {transformers.__version__}")
print(f"supabase: {supabase.__version__}")
assert torch.cuda.is_available()
print(f"device: {torch.cuda.get_device_name(0)} ({torch.cuda.device_count()} GPU(s))")
PY

# ─────────────────────────────────────────────────────────────────────
# Write scripts/prep.env — prep-only env, no wallet, no bittensor.
# ─────────────────────────────────────────────────────────────────────

PREP_ENV="$INSTALL_DIR/scripts/prep.env"
echo "[prep-setup] writing $PREP_ENV"
cat > "$PREP_ENV" <<EOF
# Reliquary prep worker — generated by setup_prep.sh

# Validator endpoint — used to fetch state.checkpoint_repo_id / revision
# so prep σ values match what the miner will produce on the live ckpt.
export RELIQUARY_VALIDATOR_URL=$VALIDATOR_URL

# Cold-start checkpoint (used only if validator has no published ckpt yet).
export RELIQUARY_CHECKPOINT=Qwen/Qwen3-4B-Instruct-2507

# Match the validator's attention impl bit-for-bit.
export GRAIL_ATTN_IMPL=flash_attention_2

# HF token to pull validator's published checkpoint.
export HF_TOKEN=$HF_TOKEN
export HUGGING_FACE_HUB_TOKEN=$HF_TOKEN

# Paths.
export RELIQUARY_INSTALL_DIR=$INSTALL_DIR
export RELIQUARY_VENV=$VENV_DIR

# OpenMath shards — match the miner's setting (default 2 = ~880k prompts).
export RELIQUARY_OMI_SHARDS=2

# CUDA allocator.
export PYTORCH_CUDA_ALLOC_CONF=expandable_segments:True

# Shared Supabase project — MUST match the miner's RELIQUARY_SUPABASE_URL.
export RELIQUARY_SUPABASE_URL=$SUPABASE_URL
export RELIQUARY_SUPABASE_KEY=$SUPABASE_KEY

# Sigma floor — match the miner.
export RELIQUARY_SIGMA_MIN=0.43

export PYTHONUNBUFFERED=1
EOF

echo
echo "✅ Prep setup complete."
echo
echo "Run on this box (one worker per GPU, runs forever, logs per GPU):"
echo "  cd $INSTALL_DIR"
echo "  source scripts/prep.env"
echo "  bash scripts/launch_prep.sh"
echo "  tail -F /root/prep_gpu*.log | grep --line-buffered \"status=\""
echo
echo "Optional network-only worker (no GPU; pulls accepted intel from R2):"
echo "  nohup \"\$RELIQUARY_VENV/bin/python\" scripts/scrape_intel.py \\"
echo "      --watch 60 --throttle 1.0 --lookback 20 --hotkey scrape_intel \\"
echo "      > /root/scrape_intel.log 2>&1 &"
echo
echo "Stop everything:"
echo "  pkill -f prep_prompt_outcomes ; pkill -f scrape_intel"
